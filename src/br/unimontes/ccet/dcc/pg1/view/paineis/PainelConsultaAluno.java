    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String aConsulta = tfNome.getText();
        if (aConsulta == null) {
            PreencherTabela();
            return;
        }
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        ConsultarAlunoController a = new ConsultarAlunoController();

        ArrayList<Aluno> alunos = ConsultarAlunoController.consulta(aConsulta);
        ;
        alunos.forEach((Aluno aluno) -> {
            model.addRow(new Object[]{aluno.getCpf(), aluno.getNome(), aluno.getAnoNascimento()});
        });
        jTable1.setModel(tableModel);

    }//GEN-LAST:event_jButton1ActionPerformed

